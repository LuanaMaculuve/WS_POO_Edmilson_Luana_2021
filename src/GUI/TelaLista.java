    public void filtro(String query) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(
                (DefaultTableModel) TablelaCli.getModel());

        TablelaCli.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));

    }

