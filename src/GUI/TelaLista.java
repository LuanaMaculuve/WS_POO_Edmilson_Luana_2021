    private void jbDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeleteActionPerformed
        // TODO add your handling code here:
        if (TelaPrincipal.perfil.equals("admin")) {
            TableModel model3 = TablelaCli.getModel();
            int i = TablelaCli.getSelectedRow();
            int id = (int) model3.getValueAt(i, 0);
            new InquilinoController().eliminarInq(id);
            this.txtNome.setText(null);
            txtApelido.setText(null);
            this.txtTelefone.setText(null);
        }else{
            JOptionPane.showMessageDialog(null, "OPERACAO NEGADA!");
        }
    }//GEN-LAST:event_jbDeleteActionPerformed

    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        // TODO add your handling code here:
        TableModel model3 = TablelaCli.getModel();
        int i = TablelaCli.getSelectedRow();
        int id = (int) model3.getValueAt(i, 0);
        System.out.print(id);
        new InquilinoController().updateInq(id, this.txtNome.getText(), this.txtApelido.getText(), this.txtTelefone.getText());
        this.txtNome.setText(null);
        txtApelido.setText(null);
        this.txtTelefone.setText(null);
    }//GEN-LAST:event_jbSaveActionPerformed

